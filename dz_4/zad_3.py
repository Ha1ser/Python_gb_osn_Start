tel = "+71234567890 +71234567854 +61234576890 +52134567890 +21235777890 +21234567110 +71232267890".split()

d = {}

for num in tel:
     code = num[:2]
     if code in d:
         d[code].append(num)
     else:
         d[code] = [num]
